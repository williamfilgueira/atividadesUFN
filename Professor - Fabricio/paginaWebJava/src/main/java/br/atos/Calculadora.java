package br.atos;

public class Calculadora {


//	método para dobrar valores
	public int dobrarValor(int numero) {
		return numero * 2;
	}
	
//	metodo para somar dois valores
	public int somarValor(int numero1, int numero2) {
		return numero1 + numero2;
	}
//	metodo para subtrair dois valores
	public int subtrairValor(int numero1, int numero2) {
		return numero1 - numero2;
	}
//	metodo para multiplicar dois valores
	public int multiplicarValor(int numero1, int numero2) {
		return numero1 * numero2;
	}
//	metodo para dividir dois valores
	public int dividirValor(int numero1, int numero2) {
		return numero1 / numero2;
	}
	
}
